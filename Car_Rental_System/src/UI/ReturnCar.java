    private void txtcarIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcarIDActionPerformed
        // TODO add your handling code here:
        if(excuted){
            load();
            excuted=false;
        }
        String car_id = txtcarID.getSelectedItem().toString();
        ResultSet rs= new ProxyConnection().
                Select("car_id,cust_id,due,DATEDIFF(NOW(),due) as elap"
                        , "rental","car_id", car_id);
        try {
            if(rs.next()){
                txtcustid.setText(rs.getString("cust_id").trim());
                txtdate.setText(rs.getString("due").trim());
                String elap=rs.getString("elap");
                System.out.println(elap);
                int elapsed =Integer.parseInt(elap);
                if(elapsed>0){
                    txtelp.setText(elap);
                    int fine = elapsed*100;
                    txtfine.setText(String.valueOf(fine));
                }else{
                    txtelp.setText("0");
                    txtfine.setText("0");
                }
            }else{JOptionPane.showMessageDialog(this, "Car ID not Found");}
                } catch (SQLException ex) {
            Logger.getLogger(ReturnCar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtcarIDActionPerformed

