    public void table_update(){
        int c;
        ResultSet rs = new ProxyConnection().establishStatement("Select * from returncar");
        ResultSetMetaData rd;
        try {
            rd = rs.getMetaData();
            c=rd.getColumnCount();
            DefaultTableModel df= (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector vector = new Vector();
                for(int i=1;i<=c;i++){
                    vector.add(rs.getString("car_id"));
                    vector.add(rs.getString("cust_id"));
                    vector.add(rs.getString("return_date"));
                    vector.add(rs.getString("elap"));
                    vector.add(rs.getString("fine"));
            }
                df.addRow(vector);
        }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void load(){
        txtcarID.removeAllItems();
        ResultSet rs = new ProxyConnection().
                establishStatement("Select * from carregistration where available='$aval'"
                .replace("$aval","No"));
        try {
            while(rs.next()){
                txtcarID.addItem(rs.getString("car_no"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnCar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
